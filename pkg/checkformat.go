package pkg

import "bytes"

// All possible Tetris figures variations
var FIGURES [][][]byte = [][][]byte{
	{{35, 35, 35}, {46, 35, 46}},
	{{35, 46}, {35, 35}, {35, 46}},
	{{46, 35, 46}, {35, 35, 35}},
	{{46, 35}, {35, 35}, {46, 35}},
	{{35, 35}, {35, 35}},
	{{35}, {35}, {35}, {35}},
	{{35, 35, 35, 35}},
	{{35, 35, 46}, {46, 35, 35}},
	{{46, 35}, {35, 35}, {35, 46}},
	{{46, 35, 35}, {35, 35, 46}},
	{{35, 46}, {35, 35}, {46, 35}},
	{{46, 35}, {46, 35}, {35, 35}},
	{{35, 35, 35}, {46, 46, 35}},
	{{35, 35}, {35, 46}, {35, 46}},
	{{35, 46, 46}, {35, 35, 35}},
	{{35, 46}, {35, 46}, {35, 35}},
	{{46, 46, 35}, {35, 35, 35}},
	{{35, 35}, {46, 35}, {46, 35}},
	{{35, 35, 35}, {35, 46, 46}},
}

func CheckFormat(tetris [][]byte) bool {
	for _, p := range FIGURES {
		if len(p) == len(tetris) {
			for i := range p {
				if !bytes.Equal(p[i], tetris[i]) {
					break
				}
				if len(p)-1 == i {
					return true
				}
			}
		}
	}
	return false
}
